ГБПОУ «Тверской колледж имени А.Н.Коняева» – одно из старейших средних специальных учебных заведений России, его история насчитывает более 140 лет.
                            
Учебное заведение было основано в октябре 1877 г. как ремесленная школа Тверского благотворительного общества «Доброхотной копейки». Главной целью создания в г. Твери учебного заведения подобного рода было предоставление возможности широким слоям городского населения получить общее и профессиональное образование. По мнению учредителей, профессиональное обучение неразрывно связано с нравственным воспитанием, что определило еще одну цель, поставленную перед ремесленной школой, – «сохранять нравственность учащихся неиспорченной».
С первых лет своего существования ремесленная школа, впоследствии неоднократно реорганизованная, заняла особое место в системе профессионального образования г. Твери, обеспечивая промышленные предприятия Тверского края квалифицированными рабочими кадрами.
